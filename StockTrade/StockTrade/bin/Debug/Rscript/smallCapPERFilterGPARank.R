filter<-function(data){
  dat<-data[관리여부!="관리종목"]
  q<-quantile(dat$시가총액,probs=seq(0,1,0.2))
  dat<-dat[시가총액<=q[2]]
  dat<-dat[PER>=0]
  dat<-dat[is.na(GPA)==FALSE]
  return(dat)
}
orderData<-function(data){
  data[,GPARk:=rank(-data$GPA)]
  data[,sizeRk:=rank(data$시가총액)]
  data[,totalRk:=GPARk+sizeRk]
  setorder(data,totalRk)
  return(data)
}

